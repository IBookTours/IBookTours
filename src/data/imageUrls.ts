/**
 * Get a random image from a category
 */
export function getRandomImage(
  category: keyof typeof IMAGES,
  subcategory?: string
): string {
  const categoryImages = IMAGES[category];

  if (subcategory && typeof categoryImages === 'object' && subcategory in categoryImages) {
    const subcategoryImages = categoryImages[subcategory as keyof typeof categoryImages];
    if (Array.isArray(subcategoryImages) && subcategoryImages.length > 0) {
      return subcategoryImages[Math.floor(Math.random() * subcategoryImages.length)];
    }
  }

  // Flatten all images in category
  const allImages = Object.values(categoryImages).flat();
  if (allImages.length > 0) {
    return allImages[Math.floor(Math.random() * allImages.length)];
  }

  return FALLBACK_IMAGES.destination;
}

/**
 * Get image with custom dimensions
 */
export function getImageWithSize(
  url: string,
  width: number,
  height: number,
  quality = 80
): string {
  // If it's an Unsplash URL, update the size parameters
  if (url.includes('unsplash.com')) {
    return url.replace(
      /w=\d+&h=\d+&fit=crop&q=\d+/,
      `w=${width}&h=${height}&fit=crop&q=${quality}`
    );
  }
  return url;
}

