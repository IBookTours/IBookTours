// Helper function to get featured vehicles
export function getFeaturedVehicles(limit = 4): CarRentalVehicle[] {
  return carRentalVehicles
    .filter(v => v.featured && v.available)
    .slice(0, limit);
}

// Helper function to get vehicles by category
export function getVehiclesByCategory(category: string): CarRentalVehicle[] {
  if (category === 'all') return carRentalVehicles.filter(v => v.available);
  return carRentalVehicles.filter(v => v.category === category && v.available);
}

// Helper function to get a single vehicle by ID
export function getVehicleById(id: string): CarRentalVehicle | undefined {
  return carRentalVehicles.find(v => v.id === id);
}
